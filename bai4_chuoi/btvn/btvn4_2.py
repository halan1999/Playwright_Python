 
 	
# 2. Cho chuỗi:  name = "tên bạn"
# In ra chữ hoa toàn bộ.
# In ra chữ thường toàn bộ.
# In ra dạng capitalize (chữ cái đầu viết hoa).
# In ra dạng title (mỗi từ viết hoa chữ cái đầu).
name="tran minh hang"
print(name.upper()) # in ra toàn bộ chữ hoa
print(name.lower()) #in ra toàn bộ chữ thường
print(name.capitalize()) #in ra chữ cái đầu viết hoa
print(name.title()) #in ra mỗi từ viết hoa chữ cái đầu