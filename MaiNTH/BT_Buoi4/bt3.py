# Viết chương trình cắt chuỗi:
s = "Automation Testing"
# Lấy ra từ "Automation"
print(s[0:10])  # Output: "Automation"
# Lấy ra từ "Testing"
print(s[11:18])  # Output: "Testing"
# Lấy ra 5 ký tự đầu tiên của chuỗi
print(s[:5])  # Output: "Autom"
# Lấy ra 3 ký tự cuối cùng của chuỗi
print(s[-3:])  # Output: "ing"