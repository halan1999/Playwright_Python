# In ra bảng cửu chương 2 và 3

# Bảng cửu chương 2, thì sẻ có cấu trúc là 2 x i = kết quả (tuong tu với 3)
# Vậy sẻ dùng range (1, 11) và dùng for để chạy 

# Bảng cưu chương 2
print("Bảng cửu chương 2: ")
for i in range(1,11): #đặt biến i để thực hiện gán giá trị
    print(f"2 x {i} = { 2 * i }") 

# Bảng cưu chương 3
print("\nBảng cửu chương 3: ")
for i in range(1,11): #đặt biến i để thực hiện gán giá trị
    print(f"3 x {i} = { 3 * i }") 
