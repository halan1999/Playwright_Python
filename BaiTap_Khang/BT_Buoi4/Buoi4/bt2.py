# 2. Cho chuỗi:  name = "tên bạn"
# In ra chữ hoa toàn bộ.
# In ra chữ thường toàn bộ.
# In ra dạng capitalize (chữ cái đầu viết hoa).
# In ra dạng title (mỗi từ viết hoa chữ cái đầu).

name = "Khang Nguyen"

upperName = name.upper()
print(f"Uppercased name: {upperName}\n")
lowerName = name.lower()
print(f"Lowercased name: {lowerName}\n")
capName = name.capitalize()
print(f"Capitalized name: {capName}\n")
titleName = name.title()
print(f"Titled name: {titleName}")