#2. Cho chuỗi:  name = "tên bạn"
# In ra chữ hoa toàn bộ.
# In ra chữ thường toàn bộ.
# In ra dạng capitalize (chữ cái đầu viết hoa).
# In ra dạng title (mỗi từ viết hoa chữ cái đầu).
name = "tên bạn"
print(f"Chuỗi chữ hoa toàn bộ:",name.upper())
print(f"Chuỗi chữ hoa thường:",name.lower())
print(f"Chuỗi chữ cái đầu viết hoa:",name.capitalize())
print(f"Chuỗi viết hoa chữ cái đầu mỗi từ:",name.title())