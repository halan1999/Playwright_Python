# "2. Cho chuỗi:  name = ""tên bạn""
# In ra chữ hoa toàn bộ.
# In ra chữ thường toàn bộ.
# In ra dạng capitalize (chữ cái đầu viết hoa).
# In ra dạng title (mỗi từ viết hoa chữ cái đầu)."

name = "Nguyễn Thị Thanh Ngân"

print("Tên viết hoa toàn bộ:", name.upper())
print("Tên viết thường toàn bộ:", name.lower())
print("Tên viết hoa ký tự đầu tiên:", name.capitalize())
print("Tên viết hoa ký tự đầu mỗi từ:", name.title())
