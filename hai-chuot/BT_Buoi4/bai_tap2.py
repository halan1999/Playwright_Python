# NOTE BÀI 2: Cho chuỗi:  name = "tên bạn"
# In ra chữ hoa toàn bộ.
# In ra chữ thường toàn bộ.
# In ra dạng capitalize (chữ cái đầu viết hoa).
# In ra dạng title (mỗi từ viết hoa chữ cái đầu).

name = "Phạm Đức Hải"

print(f"Chuỗi in hoa toàn bộ: {name.upper()}")
print(f"\nChuỗi in thường toàn bộ: {name.lower()}")
print(f"\nChuỗi in dạng chữ cái đầu tiên viết hoa: {name.capitalize()}")
print(f"\nChuỗi in dạng mỗi từ viết hoa chữ cái đầu: {name.title()}")