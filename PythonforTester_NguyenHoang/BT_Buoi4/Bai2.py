# Cho chuỗi name = "tên bạn"
name = "hOànG HạNH nGuYêN"

# In ra chữ hoa toàn bộ
print("Tên viết bằng chữ in hoa:" , name.upper())

# In ra chữ thường toàn bộ
print("Tên viết bằng chữ thường:" , name.lower())

# In ra dạng capitalize (chữ cái đầu viết hoa)
print("Tên với chữ cái đầu viết hoa:" , name.capitalize())

# In ra dạng title (mỗi từ viết hoa chữ cái đầu)
print("Tên với mỗi từ viết hoa chữ cái đầu:" , name.title())